function timeToGtfs(time) {
  return `${time}:00`;
}

function insertMany(db, sql, rows) {
  const stmt = db.prepare(sql);
  for (const row of rows) stmt.run(...row);
}

const db = new DatabaseSync(DB_PATH);
db.exec('PRAGMA foreign_keys = ON; PRAGMA busy_timeout = 5000;');

const existing = db.prepare(
  'SELECT id, name, source_url FROM gtfs_feeds WHERE source_url = ? OR name = ? LIMIT 1',
).get(SOURCE_URL, FEED_NAME);

if (existing) {
  console.log(`skip: ${existing.name} is already imported (${existing.id})`);
  db.close();
  process.exit(0);
}

const feedId = randomUUID();
db.exec('BEGIN');
try {
  db.prepare(
    'INSERT INTO gtfs_feeds (id, name, source_url, imported_at, stop_count, trip_count) VALUES (?, ?, ?, ?, ?, ?)',
  ).run(feedId, FEED_NAME, SOURCE_URL, IMPORTED_AT, stops.length, 0);

  insertMany(
    db,
    'INSERT INTO gtfs_stops (feed_id, stop_id, stop_name, lat, lng) VALUES (?, ?, ?, ?, ?)',
    stops.map(([id, name, lat, lng]) => [feedId, id, name, lat, lng]),
  );

  insertMany(
    db,
    'INSERT INTO gtfs_routes (feed_id, route_id, short_name, long_name, route_type) VALUES (?, ?, ?, ?, ?)',
    routes.map(([id, name]) => [feedId, id, name, '那須高原観光周遊バス', 3]),
  );

  db.prepare(
    `INSERT INTO gtfs_calendar
      (feed_id, service_id, mon, tue, wed, thu, fri, sat, sun, start_date, end_date)
     VALUES (?, ?, 1, 1, 1, 1, 1, 1, 1, '20260401', '20261130')`,
  ).run(feedId, SERVICE_ID);

  let tripCount = 0;
  let stopTimeCount = 0;
  const tripStmt = db.prepare(
    'INSERT INTO gtfs_trips (feed_id, trip_id, route_id, service_id, headsign, direction_id) VALUES (?, ?, ?, ?, ?, ?)',
  );
  const stopTimeStmt = db.prepare(
    'INSERT INTO gtfs_stop_times (feed_id, trip_id, stop_id, stop_sequence, departure_time, arrive_time) VALUES (?, ?, ?, ?, ?, ?)',
  );

  for (const run of routeRuns) {
    for (let col = 0; col < run.columns.length; col += 1) {
      const tripId = `${run.routeId}-${run.columns[col]}`;
      tripStmt.run(feedId, tripId, run.routeId, SERVICE_ID, run.headsign, run.directionId ?? null);
      tripCount += 1;
      run.rows.forEach((row, index) => {
        const stopId = row[0];
        const t = row[col + 1];
        stopTimeStmt.run(feedId, tripId, stopId, index + 1, timeToGtfs(t), timeToGtfs(t));
        stopTimeCount += 1;
      });
    }
  }

  db.prepare('UPDATE gtfs_feeds SET trip_count = ? WHERE id = ?').run(tripCount, feedId);
  db.exec('COMMIT');
  console.log(`imported: ${FEED_NAME}`);
  console.log(`feed_id: ${feedId}`);
  console.log(`stops: ${stops.length}, routes: ${routes.length}, trips: ${tripCount}, stop_times: ${stopTimeCount}`);
} catch (error) {
  db.exec('ROLLBACK');
  throw error;
} finally {
  db.close();
}
